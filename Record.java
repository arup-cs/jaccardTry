
public class Record {
	float sepalL;
	float sepalW;
	float petalL;
	float petalW;
	String category;
	
	public Record(){

	}
	public float getSepalL() {
		return sepalL;
	}

	public void setSepalL(float sepalL) {
		this.sepalL = sepalL;
	}

	public float getSepalW() {
		return sepalW;
	}

	public void setSepalW(float sepalW) {
		this.sepalW = sepalW;
	}

	public float getPetalL() {
		return petalL;
	}

	public void setPetalL(float petalL) {
		this.petalL = petalL;
	}

	public float getPetalW() {
		return petalW;
	}

	public void setPetalW(float petalW) {
		this.petalW = petalW;
	}

	public String getCategory() {
		return category;
	}

	public void setCategory(String category) {
		this.category = category;
	}

	

	

}
